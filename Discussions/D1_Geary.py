textLines = ["Dream big, start small.", "Every step moves you closer,", "The journey is just as important as the goal."]

def printThreeLines(lineOne, lineTwo, lineThree):
	return (
		f"{lineOne}\n"
		f"{lineTwo}\n"
		f"{lineThree}"
	)

print(printThreeLines(textLines[0], textLines[1], textLines[2]))